from telegram import Update
from telegram.ext import ContextTypes
from util import send_text
import random
from menu_handler import start  # —è–∫—â–æ menu_handler —ñ–º–ø–æ—Ä—Ç—É—î messages_random ‚Äî –∑—Ä–æ–±–∏ –ª–æ–∫–∞–ª—å–Ω–∏–π —ñ–º–ø–æ—Ä—Ç –≤ show_funny_response (–¥–∏–≤. –Ω–∏–∂—á–µ)

async def interpret_random_input(update: Update, context: ContextTypes.DEFAULT_TYPE) -> bool:
    """
    –ê–Ω–∞–ª—ñ–∑—É—î —Ç–µ–∫—Å—Ç –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è —ñ –≤–∏–∫–æ–Ω—É—î –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω—É –¥—ñ—é.
    –ü–æ–≤–µ—Ä—Ç–∞—î True, —è–∫—â–æ –æ–±—Ä–æ–±–ª–µ–Ω–æ (–≤–∏–∫–æ–Ω–∞–Ω–∞ –¥—ñ—è), —ñ False —è–∫—â–æ –Ω–µ –≤–¥–∞–ª–æ—Å—è –≤–∏–∑–Ω–∞—á–∏—Ç–∏ –Ω–∞–º—ñ—Ä.
    """
    # –ó–∞—Ö–∏—Å—Ç –≤—ñ–¥ –∞–ø–¥–µ–π—Ç—ñ–≤ –±–µ–∑ —Ç–µ–∫—Å—Ç—É
    if not update.message or not update.message.text:
        return False

    message_text = update.message.text.lower().strip()

    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –Ω–∞ –≤–∏–ø–∞–¥–∫–æ–≤—ñ —Ñ–∞–∫—Ç–∏
    if any(keyword in message_text for keyword in ['—Ñ–∞–∫—Ç', '—Ü—ñ–∫–∞–≤', 'random', '–≤–∏–ø–∞–¥–∫–æ–≤']):
        # –õ–æ–∫–∞–ª—å–Ω–∏–π —ñ–º–ø–æ—Ä—Ç, —â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ —Ü–∏–∫–ª—ñ—á–Ω–∏—Ö –∑–∞–ª–µ–∂–Ω–æ—Å—Ç–µ–π –Ω–∞ —Ä—ñ–≤–Ω—ñ –º–æ–¥—É–ª—è
        from item_random import random_fact
        await send_text(update, context, "üß† –°—Ö–æ–∂–µ, –≤–∏ —Ü—ñ–∫–∞–≤–∏—Ç–µ—Å—å –≤–∏–ø–∞–¥–∫–æ–≤–∏–º–∏ —Ñ–∞–∫—Ç–∞–º–∏! –ó–∞—Ä–∞–∑ –ø–æ–∫–∞–∂—É –≤–∞–º –æ–¥–∏–Ω...")
        await random_fact(update, context)
        return True

    # –ü–µ—Ä–µ—Ö–æ–¥–∏ –≤ —Ä–µ–∂–∏–º ChatGPT
    if any(keyword in message_text for keyword in ['gpt', '—á–∞—Ç', '–ø–∏—Ç–∞–Ω–Ω—è', '–∑–∞–ø–∏—Ç–∞', '–¥—ñ–∑–Ω–∞—Ç–∏—Å—è']):
        from item_question import gpt_handler
        await send_text(update, context, "ü§ñ –°—Ö–æ–∂–µ, —É –≤–∞—Å —î –ø–∏—Ç–∞–Ω–Ω—è! –ü–µ—Ä–µ—Ö–æ–¥–∏–º–æ –¥–æ —Ä–µ–∂–∏–º—É —Å–ø—ñ–ª–∫—É–≤–∞–Ω–Ω—è –∑ ChatGPT...")
        await gpt_handler(update, context)
        return True

    # –ü–µ—Ä–µ—Ö—ñ–¥ —É —Ä–µ–∂–∏–º talk
    if any(keyword in message_text for keyword in ['—Ä–æ–∑–º–æ–≤', '–≥–æ–≤–æ—Ä–∏', '—Å–ø—ñ–ª–∫—É–≤–∞', '–æ—Å–æ–±–∏—Å—Ç', 'talk']):
        # –õ–æ–∫–∞–ª—å–Ω–∏–π —ñ–º–ø–æ—Ä—Ç item_talk —â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ —Ü–∏–∫–ª—ñ—á–Ω–æ—Å—Ç—ñ –ø—Ä–∏ —ñ–º–ø–æ—Ä—Ç—ñ –º–æ–¥—É–ª—ñ–≤
        from item_talk import talk_handler
        await send_text(update, context, "üë§ –°—Ö–æ–∂–µ, –≤–∏ —Ö–æ—á–µ—Ç–µ –ø–æ–≥–æ–≤–æ—Ä–∏—Ç–∏ –∑ –≤—ñ–¥–æ–º–æ—é –æ—Å–æ–±–∏—Å—Ç—ñ—Å—Ç—é! –ó–∞—Ä–∞–∑ –ø–æ–∫–∞–∂—É –≤–∞–º –¥–æ—Å—Ç—É–ø–Ω—ñ –≤–∞—Ä—ñ–∞–Ω—Ç–∏...")
        await talk_handler(update, context)
        return True

    return False


async def show_funny_response(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–ü–æ–∫–∞–∑—É—î –≤–∏–ø–∞–¥–∫–æ–≤—É –∫—É–º–µ–¥–Ω—É –≤—ñ–¥–ø–æ–≤—ñ–¥—å, –∫–æ–ª–∏ –Ω–∞–º—ñ—Ä –Ω–µ –≤–∏–∑–Ω–∞—á–µ–Ω–æ."""
    funny_responses = [
        "ü§î –•–º–º... –¶—ñ–∫–∞–≤–æ, –∞–ª–µ —è –Ω–µ –∑—Ä–æ–∑—É–º—ñ–≤, —â–æ —Å–∞–º–µ –≤–∏ —Ö–æ—á–µ—Ç–µ. –ú–æ–∂–µ —Å–ø—Ä–æ–±—É—î—Ç–µ –æ–¥–Ω—É –∑ –∫–æ–º–∞–Ω–¥ –∑ –º–µ–Ω—é?",
        "üßê –î—É–∂–µ —Ü—ñ–∫–∞–≤–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è! –ê–ª–µ –º–µ–Ω—ñ –ø–æ—Ç—Ä—ñ–±–Ω—ñ —á—ñ—Ç–∫—ñ—à—ñ —ñ–Ω—Å—Ç—Ä—É–∫—Ü—ñ—ó. –û—Å—å –¥–æ—Å—Ç—É–ø–Ω—ñ –∫–æ–º–∞–Ω–¥–∏:",
        "üòÖ –û–π, –∑–¥–∞—î—Ç—å—Å—è, –≤–∏ –º–µ–Ω–µ –∑–∞—Å—Ç–∞–ª–∏ –∑–Ω–µ–Ω–∞—Ü—å–∫–∞! –Ø –≤–º—ñ—é –±–∞–≥–∞—Ç–æ —á–æ–≥–æ, –∞–ª–µ –º–µ–Ω—ñ –ø–æ—Ç—Ä—ñ–±–Ω–∞ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–∞ –∫–æ–º–∞–Ω–¥–∞:",
        "ü§ñ *–ø–µ—Ä–µ–∑–∞–≤–∞–Ω—Ç–∞–∂—É—î—Ç—å—Å—è* –í–∏–±–∞—á—Ç–µ, –º–æ—ó –∞–ª–≥–æ—Ä–∏—Ç–º–∏ –Ω–µ —Ä–æ–∑–ø—ñ–∑–Ω–∞–ª–∏ —Ü–µ —è–∫ –∫–æ–º–∞–Ω–¥—É. –û—Å—å —â–æ —è —Ç–æ—á–Ω–æ –≤–º—ñ—é:",
        "ü¶Ñ –¶–µ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è —Ç–∞–∫–µ –∂ –∑–∞–≥–∞–¥–∫–æ–≤–µ, —è–∫ —î–¥–∏–Ω–æ—Ä—ñ–≥ —É –¥–∏–∫—ñ–π –ø—Ä–∏—Ä–æ–¥—ñ! –°–ø—Ä–æ–±—É–π—Ç–µ –æ–¥–Ω—É –∑ —Ü–∏—Ö –∫–æ–º–∞–Ω–¥:",
    ]

    hints = [
        "–°–ø—Ä–æ–±—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É /gpt, —â–æ–± –∑–∞–¥–∞—Ç–∏ –ø–∏—Ç–∞–Ω–Ω—è",
        "–í–∏–∫–æ—Ä–∏—Å—Ç–∞–π—Ç–µ /random –¥–ª—è –æ—Ç—Ä–∏–º–∞–Ω–Ω—è —Ü—ñ–∫–∞–≤–æ–≥–æ —Ñ–∞–∫—Ç—É",
        "–ö–æ–º–∞–Ω–¥–∞ /talk –¥–æ–∑–≤–æ–ª–∏—Ç—å –≤–∞–º –ø–æ—Å–ø—ñ–ª–∫—É–≤–∞—Ç–∏—Å—è –∑ –≤—ñ–¥–æ–º–æ—é –æ—Å–æ–±–∏—Å—Ç—ñ—Å—Ç—é",
        "–ù–µ –∑–Ω–∞—î—Ç–µ, —â–æ –æ–±—Ä–∞—Ç–∏? –ü–æ—á–Ω—ñ—Ç—å –∑ /start",
    ]

    response = f"{random.choice(funny_responses)}\n\nüí° *–ü—ñ–¥–∫–∞–∑–∫–∞:* {random.choice(hints)}"
    await send_text(update, context, response)

    # –ª–æ–∫–∞–ª—å–Ω–∏–π —ñ–º–ø–æ—Ä—Ç start (—â–æ–± —É–Ω–∏–∫–Ω—É—Ç–∏ —Ü–∏–∫–ª—ñ—á–Ω–∏—Ö —ñ–º–ø–æ—Ä—Ç—ñ–≤, —è–∫—â–æ menu_handler —ñ–º–ø–æ—Ä—Ç—É—î messages_random)
    try:
        from menu_handler import start as menu_start
        # –ø–æ–∫–∞–∑—É–≤–∞—Ç–∏ –º–µ–Ω—é –Ω–µ –∑–∞–≤–∂–¥–∏ ‚Äî —Ç—É—Ç –ø–æ–∫–∞–∑—É—î–º–æ –∑–∞–≤–∂–¥–∏; –º–æ–∂–µ—à –æ–±–º–µ–∂–∏—Ç–∏ –π–º–æ–≤—ñ—Ä–Ω—ñ—Å—Ç—å
        await menu_start(update, context)
    except Exception:
        # —è–∫—â–æ –Ω–µ –º–æ–∂–Ω–∞ —ñ–º–ø–æ—Ä—Ç—É–≤–∞—Ç–∏, –ø—Ä–æ—Å—Ç–æ –ø—Ä–æ–ø—É—Å–∫–∞—î–º–æ (—â–æ–± –Ω–µ –ø–∞–¥–∞—Ç–∏)
        pass
